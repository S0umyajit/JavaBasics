package org.example.basics;

public class FirstTest {
    public static void main(String[] args) {
        //Escape Sequence
        System.out.println("Hello \"Soumyajit\" World");
        System.out.println("Hello \'Soumyajit\' World");
        System.out.println("Hello \nSoumyajit\nWorld");
        System.out.println("Hello \bSoumyajit World");
        System.out.println("Hello\tSoumyajit World");
    }
}
