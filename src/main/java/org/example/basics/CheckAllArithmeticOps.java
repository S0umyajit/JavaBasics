package org.example.basics;

public class CheckAllArithmeticOps {

    public static void main(String[] args) {

        int a=12;
        int b=6;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
    }
}
